    <h2>
      - Aucune selle ne convient à mon Dracaufeu, que faire ?
    </h2>
    <p>
      Si la selle est un élément que ne supporte pas votre Dracaufeu, il est impératif que vous ne le brusquiez pas ! Nous vous recommandons d\'utiliser une méthode de renforcement positif (méthode trouvable sur internet) et d\'être patient avec votre ami ! Si la selle est trop petite, même si c\'est la plus grande taille, c\'est sûrement parce que votre Pokémon est en surpoids !
      Notre solution est de le mettre au footing ou, pour une méthode plus extrême, aux combats. Cependant, il faudra également faire appel à un Pokédiéteticien afin qu\'il surveille l\'alimentation de votre Dracaufeu. 
    </p>
  </div>

