    <div>Vous serez obligé d\'assister à une séance de prévention d\'abandon des animaux, de nous payer une aide mensuelle pour l\'aide d\'un Poképsychologue, nous donner votre âme, ainsi que de rédiger une lettre d\'excuses à votre Dracaufeu.</div>
