    <h2>
      - Mon Dracaufeu refuse de se nourrir, que faire ?
    </h2>
    <p>
      Pour palier à ce problème essayez d\'en identifier la cause, est-il triste ? en manque d\'affection ? malade ? Si vous avez répondu oui à une de ces questions, alors essayez de résoudre le problème en prenant soin de lui. Sinon, il est possible que votre Dracaufeu fasse l\'experience d\'une période de nostalgie. En effet, il est courant que nos Dracaufeu se rappelent de leur bon temps passé avant d\'arriver chez vous, ainsi nous vous conseillons de lui changer les idées ! Vous pouvez lui faire rencontrer d\'autres Pokémon, lui faire faire des combats (avec modération) ou bien commander un compagnon de jeu sur notre site !
    </p>
  </div>

  <div id="sept"><p>•</p></div>
  <div class="questionEtReponse">
    <h2>
      - Mon Dracaufeu est agressif, que faire ?
    </h2>
    <p>
      Nos Dracaufeu ne sont jamais agressifs sauf s\'ils sont blessés, maltraités ou bien ont un fort sentiment de rejet. 
      Une des solutions et de lui donner beaucoup d\'affection en lui disant des mots encourageants, en le caressant ou en lui offrant de la nourriture (attention à ne pas abuser de cette dernière méthode !). Si, malgré cela, il vous est impossible de le garder ou qu\'il continue à être agressif à votre égard, nous vous conseillons de rester loin de lui, puis de contacter notre service après vente afin que nous puissions le récuperer et nous en occuper à nouveau. 
    </p>
  </div>

