      - Que fait votre entreprise ?
    </h2>
    <p>
      Notre entreprise gère plusieurs domaines à la fois. Nous gérons la naissance du Salamèche, son éducation, sa santé, ainsi que plusieurs autres facteurs qui font qu\'un petit Salamèche deviendra un grand et majestieux Dracaufeu. Ils seront nourris, entrainés, soignés afin qu\'ils évoluent dans le meilleur milieu possible et le plus rapidement.
        Plus d\'informations sur la page <a href="produit.html#egg_block">produit</a>.
    </p>
