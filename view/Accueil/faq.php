      - Puis-je revendre mon Dracaufeu ?
    </h2>

    <div>Si vous souhaitez revendre votre Dracaufeu c\'est probablement parce que vous n\'avez plus de place, plus assez d\'argent pour vous en occuper ou tout simplement que vous êtes une horrible personne. Dans tous les cas, il est fortement déconseillé de revendre votre Dracaufeu, puisqu\'il se sentira abandonné et mal aimé, vous ne souhaitez pas ça, si ?</div>
